# String to integer
'12'.to_i # 12
'4'.to_i # 4
'4 hi there'.to_i # 4
'hi there 4'.to_i # 0

# String to float
'4'.to_f # 4.0
'4 hi there'.to_f # 4.0
'hi there 4'.to_f # 0.0

# Integer to String
4.to_s # '4'

# Float to Integer
4.0.to_i # 4
4.9.to_i # 4

# Float to String
4.0.to_s
